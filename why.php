                                        <p>Simply put, we care, we are current with everything available in the industry and we are good value for money, BUT let us show you why.</p>

                                        <p>Our view is that a Settlement is the process of transferring land from the Seller to the Purchaser, once all conditions have been for filled, in exchange for consideration.  The process of achieving Settlement is unique as each property is heterogeneous and each set of Seller and Purchaser circumstances are also different, so you need a representative who understands this and works with you to achieve Settlement. This process commences from when the offer and acceptance contract is signed and generally ends on the day of Settlement.  Most offer and acceptance contracts are subject to various conditions such as:</p>

                                        <ul>
                                           <li>Unconditional finance approval;</li>
                                           <li>Building inspection reports;</li>
                                           <li>Timber pest inspection reports;</li>
                                           <li>Issue of independent certificate of titles; and</li>
                                           <li>Foreign investment review board approval, to name a few.</li>
                                        </ul>

                                        <p>Its important to elect a representative who understands and is familiar with the conditions so your best interests are protected. At  Plus Your Settlements, we have refined these skills to make this process as simple as possible. Once the conditions have been meet or waived and your matter becomes unconditional we set a date to Settlement and at all times keep you informed. Once your matter is unconditional a time period of 14 -28 days is required to ensure that all parties are ready to complete Settlement. On the day of Settlement, legal and financial documents are exchanged for monies and Settlement is complete.</p>

                                        <p>The process of Settlement we have outlined above may seem simple in nature but due to the uniqueness of each transaction we act in, we find dealing in your most prized possession being your current home, new home or new investment property requires attention to detail, diligence and most importantly the time to set aside and deal with your matter. At Plus Your Settlements we are a boutique Settlement agent located on the cusp of the Swan and Avon Valley in Midland and we pride ourselves in taking an interest in your matter rather than treating you like a number.  We would love to hear from you so please contact us so we can provide you a tailored quote.</p>

